def generate_daily_horoscopes():
    random.shuffle(tarot_cards)
    random.shuffle(focus_list)
    horoscopes = []
    for i, sign in enumerate(signs):
        forecast = generate_forecast()
        card = tarot_cards[i % len(tarot_cards)]
        focus = focus_list[i % len(focus_list)]
        compatibility = ", ".join(random.sample([s for s in signs if s != sign], 2))
        horoscopes.append(horoscope_template(sign, forecast, card, focus, compatibility))
    return horoscopes

